# i) if statement
x = 10
if x > 0:
    print("x is positive")

# ii) if else statement
y = -5
if y > 0:
    print("y is positive")
else:
    print("y is non-positive")

# iii) if – elif – else statement
grade = 75
if grade >= 90:
    print("Grade is A")
elif grade >= 80:
    print("Grade is B")
elif grade >= 70:
    print("Grade is C")
elif grade >= 60:
    print("Grade is D")
else:
    print("Grade is F")
